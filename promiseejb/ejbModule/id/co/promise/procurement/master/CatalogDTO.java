package id.co.promise.procurement.master;

public class CatalogDTO {
	
	
	private Integer id;
	private Integer qtyCartItem;
	private Integer harga_eproc;
	private double harga;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getQtyCartItem() {
		return qtyCartItem;
	}

	public void setQtyCartItem(Integer qtyCartItem) {
		this.qtyCartItem = qtyCartItem;
	}

	public Integer getHarga_eproc() {
		return harga_eproc;
	}

	public void setHarga_eproc(Integer harga_eproc) {
		this.harga_eproc = harga_eproc;
	}

	public double getHarga() {
		return harga;
	}

	public void setHarga(double harga) {
		this.harga = harga;
	}
	
	
	
}
